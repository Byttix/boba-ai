def save_training_data(data: List[Dict], filename: str = "training_data.json"):
    with open(filename, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"Создано {len(data)} примеров для обучения")
    print(f"Данные сохранены в {filename}")

    csv_filename = filename.replace('.json', '.csv')
    with open(csv_filename, 'w', encoding='utf-8', newline='') as f:
        writer = csv.writer(f)
        writer.writerow(['text', 'intent', 'confidence'])
        for item in data:
            writer.writerow([item['text'], item['intent'], item['confidence']])

    print(f"CSV версия сохранена в {csv_filename}")

def analyze_intents(data: List[Dict]):
    intent_counts = {}
    for item in data:
        intent = item['intent']
        intent_counts[intent] = intent_counts.get(intent, 0) + 1

    print("\nРаспределение намерений:")
    for intent, count in sorted(intent_counts.items()):
        percentage = (count / len(data)) * 100
        print(f"  {intent}: {count} примеров ({percentage:.1f}%)")

